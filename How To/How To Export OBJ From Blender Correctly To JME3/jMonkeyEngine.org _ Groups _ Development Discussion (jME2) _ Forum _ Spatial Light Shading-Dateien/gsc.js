jQuery(document).ready(function($) {
		   
	// Dialog			
	$('#dialog').dialog({
		autoOpen: false,
		width: 600,
		modal: true,
	});

	// Dialog Link
	$('#dialog_link').click(function(){
		$('#dialog').dialog('open');
		return false;
	});
	
	//hover states on the static widgets
	$('#dialog_link, ul#icons li').hover(
		function() { $(this).addClass('ui-state-hover'); }, 
		function() { $(this).removeClass('ui-state-hover'); }
	);
	
	$( "#dialog" ).dialog({

	});
	
});
