    public void updateFoodSources(ArrayList<FoodSource> sources) {
        javax.swing.DefaultListModel model = (javax.swing.DefaultListModel) jList1.getModel();
        model.clear();
        for (int i = 1; i <= sources.size(); i++) {
            model.addElement("FoodSource" + i);
        }
        updateUI();
    }

