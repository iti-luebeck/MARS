    /**
     * 
     * @param map_panel
     */
    private void addAWTMapPanel(AwtPanel map_panel){
        VideoPanel.add(map_panel);
    }
    
    private void cleanup(){
        mars.enqueue(new Callable<Void>(){
            public Void call(){
                lastSelectedVideoCamera.cleanupDebugView();
                return null;
            }
        });
        VideoPanel.removeAll();
    }

